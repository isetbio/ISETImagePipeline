% Unpack data for plotting
%
% Should add some asserts to make sure data format doesn't change in the
% future.  Assumptions about that hard coded in this loop.
nRatios = size(UYDataVSize{1},2);
for ii = 1:length(UYDataVSize)
    for rr = 1:nRatios
        useRr = rr; %nRatios-rr+1;
        mosaicRatio(rr) = UYDataVSize{ii}(3,useRr);
        plotDataSize{rr}(ii) = UYDataVSize{ii}(1,useRr);
        plotData580RefUY{rr}(ii) = UYDataVSize{ii}(4,useRr);
        plotData570RefUY{rr}(ii) = UYDataVSize{ii}(8,useRr);
    end
end


nSubRows = 2;
nSubCols = 3;
figure; clf; hold on;
set(gcf,'Position',[10 10 1200 800]);
for rr = 1:nRatios
    subplot(nSubRows,nSubCols,rr); hold on;
    plot(plotDataSize{rr},plotData570RefUY{rr},'ro','MarkerFaceColor','r','MarkerSize',12);
    plot(plotDataSize{rr},plotData580RefUY{rr},'bo','MarkerFaceColor','b','MarkerSize',12);
    plot(plotDataSize{rr},plotData570RefUY{rr},'r','LineWidth',2);
    plot(plotDataSize{rr},plotData580RefUY{rr},'b','LineWidth',2);
    plot([0 12],[570 570],'r:');
    plot([0 12],[580 580],'b:');
    xlabel('Square size (min)','FontSize',18);
    ylabel('Unique Yellow Equiv Wl (nm)','FontSize',18);
    title(sprintf('%0.2f L:M mosaic',mosaicRatio(rr)),'FontSize',20);
    legend('570 nm reference','580 nm reference','FontSize',14);
    xlim([0 12]); ylim([560 630]);
